# A simple madlib program that asks the user for input and then prints out a story.

# Ask the user for input
print("Please enter a noun:")
noun = input()

print("Please enter a verb:")
verb = input()

print("Please enter an adjective:")
adjective = input()

print("Please enter an adverb:")
adverb = input()

# Print out the story
print(f"Do you {verb} your {adjective} {noun} {adverb}? That's hilarious!")

# End of program
